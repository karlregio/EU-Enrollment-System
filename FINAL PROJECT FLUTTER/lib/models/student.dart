class Student {
  final String name;
  final String course;
  final String term;
  final String credentials;
  final String status;
  final String password;
  final String studentType;
  final String major;

  Student({
    required this.name,
    required this.course,
    required this.term,
    required this.credentials,
    required this.status,
    required this.password,
    required this.studentType,
    required this.major,
  });
}